package Exercises.Methods.AdvancedCalc;

public class AdvancedCalculator {
    public static void performCalculations(int num1, int num2) {
        System.out.println(num1 + num2);
        System.out.println(num1 - num2);
        System.out.println(num1 * num2);
        System.out.println((double) num1 / num2);
    }
}
